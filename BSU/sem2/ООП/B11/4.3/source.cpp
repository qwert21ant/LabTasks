#include "header.h"

void print_usage() {
	cout << "Commands:" << endl;
	cout << "  genereate - generates random tasks" << endl;
	cout << "  remove - removes task (0-indexing)" << endl;
	cout << "  clear - removes all tasks" << endl;
	cout << "  print - print all tasks" << endl;
	cout << "  exit - save tasks and terminate program" << endl;
	cout << endl;
}