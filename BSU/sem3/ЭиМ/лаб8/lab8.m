function [] = graph(ind, n, a, b, c, d, ttl, yl)
  f = figure(ind);
  plot(n, a, 'ro-', n, b, 'go-', n, c, 'bo-', n, d, 'mo-');
  title(['Зависимость ', ttl, ' от времени']);
  xlabel('n');
  ylabel(yl);
  legend('R1', 'L1', 'C1', 'R4L4C4');
  saveas(f, ['graph', num2str(ind), '.png']);
end